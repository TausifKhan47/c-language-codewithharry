// Write a C program to calculate area of a rectangle (b)Using inputs supplied by the User
#include<stdio.h>
int main()
{
    int l,b,area;
    printf("enter the value of the length:");
    scanf("%d",&l);
    printf("enter the value of the breadth:");
    scanf("%d",&b);
    area=l*b;
    printf("the area of the rectangle is %d",area);


}